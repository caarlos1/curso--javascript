// Tipos de atribuições simples e com operações.

// Simples:
const a = 7
let b = 3

// Com operações:
b += a; // Equivale: b = b + a
console.log(b);

b -= 4; // Equivale: b = b * 4 
console.log(b);

b *= 4; // Equivale: b = b * 4 
console.log(b);

b /= 4; // Equivale: b = b / 4 
console.log(b);

// Aqui o % representa o modulo.
b %= 4; // Equivale: b = b % 4 
console.log(b);
